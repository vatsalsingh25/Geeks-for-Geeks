class Solution {
  public:
    int SumofMiddleElements(vector<int> &arr1, vector<int> &arr2) {
        // code here
        int n=arr1.size();
        int l=0;
        int r=0;
        while(1){
            if(arr1[l]<=arr2[r]) l++;
            else r++;
            
            if(l+r==n-1) break;
        }
        int ans1 = arr1[l]+arr2[r];
        int ans2 = arr2[r]+arr2[r+1];
        int ans3 = arr1[l]+arr1[l+1];
        
        int ans= min(ans3,min(ans1,ans2));
        return ans;
    }
};

